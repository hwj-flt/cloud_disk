package com.dgut.cloud_disk.pojo.vo;

public class ReStorageFileVo {
    private  String token;
    private  String shareID;
    private  String newDirectID;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getShareID() {
        return shareID;
    }

    public void setShareID(String shareID) {
        this.shareID = shareID;
    }

    public String getNewDirectID() {
        return newDirectID;
    }

    public void setNewDirectID(String newDirectID) {
        this.newDirectID = newDirectID;
    }
}
